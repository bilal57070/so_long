#include "ft_printf.h"

int	ft_putstr(char *s)
{
	int	i;

	i = 0;
	if (s != NULL)
	{
		while (s[i])
			i++;
		write(1, s, i);
	}
	else
	{
		ft_putstr("(null)");
		return (6);
	}
	return (i);
}
